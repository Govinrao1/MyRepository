package mypack;

public class Example24
{
	public static void main(String[] args)
	{
		int n1 = 31;
		int n2 = 031; //octal
		int n3 = 0x31; //hexadecimal
		int n4 = 0b1001; //binary
		System.out.println(n1); //31
		System.out.println(n2); //25
		System.out.println(n3); //49
		System.out.println(n4); //9
	}
}
